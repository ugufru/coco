\
\ Provides: init-font, glyph-addr
\ Requires: kernel primitives C!, +, -, *, IF/THEN, DO/LOOP
\
\ 59 glyphs covering ASCII $20-$5A (space, punctuation, 0-9, A-Z).
\ 8 bytes per glyph (7 visible + 1 blank spacer row).
\ Each pixel is a 2-bit artifact pair (11=on, 00=off) — no NTSC fringing.
\ 4th artifact pixel always 00 (inter-character gap).
\
\ Design rules:
\   - Horizontal bars (top/middle/bottom): ### ($FC) — full width OK
\   - Vertical stems: #.. ($C0) or ..# ($0C) — single artifact pixel
\   - Both sides: #.# ($CC)
\   - Transitions (elbows/legs): ##. ($F0) or .## ($3C) — max one row
\   - Thin centered: .#. ($30) — for I, T, Y, 1, 7 stems

VARIABLE fp

: fb  ( byte -- )  fp @ C!  fp @ 1 + fp ! ;
: f8  ( b7 b6 b5 b4 b3 b2 b1 b0 -- )  fb fb fb fb fb fb fb fb ;

: glyph-addr  ( char -- addr )
  DUP $20 < IF DROP $20 THEN
  DUP $5A > IF DROP $20 THEN
  $20 - 8 * $7000 + ;

\ Helper: write 8 blank bytes (unused glyph slot)
: f0  ( -- )  $00 $00 $00 $00 $00 $00 $00 $00 f8 ;

: init-font
